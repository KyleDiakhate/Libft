#include "libft.h"

int	ft_atoi(const char *nptr)
{
	int	i;
	int	neg;
	int	num;

	neg = 1;
	i = 0;
	num = 0;
	while ((nptr[i] >= '\t' && nptr[i] <= '\n') || (nptr[i] == ' '))
	{
		i++;
	}
	if (nptr[i] == '-' || nptr[i] == '+')
	{
		if (nptr[i] == '-')
			neg *= -1;
		i++;
	}
	while (nptr[i] >= '0' && nptr[i] <= '9')
	{
		num = num * 10 + (nptr[i] - '0');
		i++;
	}
	return (num * neg);
}
